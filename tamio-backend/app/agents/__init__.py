# Tamio Agent System
#
# This module contains the agent implementations for Tamio's cash flow management system.
#
# Agents:
# 1. Forecast Engine - Projects 13-week cash position
# 2. Cash Flow Vigilance Agent - Monitors for shortfalls & risks
# 3. AR & Invoice Agent - Generates invoices, drafts collection emails
# 4. Vendor & Payment Agent - Ranks vendors for delays, drafts messages
# 5. Payment Batch Sequencer - Optimizes payment timing across week
# 6. Excess Cash Allocator - Suggests allocation for surplus cash
#
# See AGENTS_ARCHITECTURE.md for detailed specifications.
